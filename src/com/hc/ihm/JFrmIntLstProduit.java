    class verifyTxtFieldString extends InputVerifier {
         @Override
         public boolean verify(JComponent input) {
             JTextField tf = (JTextField) input;
             boolean bOk = !tf.getText().isEmpty();
     //        jLblErreur.setLocation(tf.getLocation().x,tf.getLocation().y+230);
     //        jLblErreur.paint(tf.getGraphics());
             jLblErreur.setVisible(!bOk);


             return (bOk);
         }
    }
    
    class verifyTxtFieldInt extends InputVerifier {
         @Override
         public boolean verify(JComponent input) {
             boolean bOk = true;
             JTextField tf = (JTextField) input;
             try {
                 int i = Integer.parseInt(tf.getText()); 
                 if (i<=0)
                     bOk=false;
             }
             catch (Exception e) {
                 bOk = false;
             }
             jLblErreur.setVisible(!bOk);
             return bOk;
         }
    }

    class verifyTxtFieldDouble extends InputVerifier {
         @Override
        public boolean verify(JComponent input) {
            boolean bOk = true;
            JTextField tf = (JTextField) input;
            try {
                double d = Double.parseDouble(tf.getText());                 
                if (d<=0) 
                    bOk=false;
            }
            catch (Exception e) {
                bOk = false;
            }
            jLblErreur.setVisible(!bOk);
            return bOk;
        }
   }
    
    
    
