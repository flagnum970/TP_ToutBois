    private void remplitTable()
    {
        String [] cols = {"Code","Désignation","Prix HT","Quantité stock"}; 
        DefaultTableModel listData = new DefaultTableModel(cols,0); 
        
        for (Produit r : hashProduit.values()) {
            String[] ts = {r.getCode(),
                r.getDesignation(),
                Double.toString(r.getPrixHT()),
                Integer.toString(r.getQteStock())};
            
            listData.addRow(ts);
        }

        jTblRep.setModel(listData);
        initTailleCols();
    }

    //Taille des colonnes
    private void initTailleCols() 
    {
        int[] tailleCol = {37,240,42,100};
        for (int i=0;i<tailleCol.length;i++)
            jTblRep.getColumnModel().getColumn(i).setPreferredWidth(tailleCol[i]);       
    }
    
    
    private void remplitFiche(String code) 
    {
        if (!code.isEmpty()) {
        Produit p = hashProduit.get(code);

        jTxtDésignation.setText(p.getDesignation());
        jTxtPrixHT.setText(String.valueOf(p.getPrixHT()));
        jTxtQuantite.setText(String.valueOf(p.getQteStock()));
        jTxtCode.setText(p.getCode());
        } else {
            jTxtDésignation.setText("");
            jTxtPrixHT.setText("");
            jTxtQuantite.setText("");
            jTxtCode.setText("");
        }
            
        jLblErreur.setVisible(false);
    }
    
    private void sauveFiche()
    { 
        if (typAcc == type_acces.modification) {
            Produit p = new Produit(   jTxtCode.getText(),
                                       jTxtDésignation.getText(),
                                       Double.parseDouble(jTxtPrixHT.getText()),    
                                       Integer.parseInt(jTxtQuantite.getText()));
                      
            hashProduit.put(jTxtCode.getText(),p);
           
        } else {
            Produit p = new Produit(   jTxtDésignation.getText(),
                                       Double.parseDouble(jTxtPrixHT.getText()),
                                       Integer.parseInt(jTxtQuantite.getText()));
                                                
            hashProduit.put("IND",p); //
        }
        

    }
        
    private void supprimeFiche() {

        //Verification de l'intégrité des données (on ne peut pas supprimer un représentant s'il est utilisé ailleurs (client ou prospect)
        if (verifIntegriteRep()) {
            //Affichage boite de dialogue pour confirmation
            if (JOptionPane.showConfirmDialog(null,"Etes-vous sûr de vouloir supprimer ce représentant ?","Attention",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                Produit r = hashProduit.get(Integer.parseInt(jTxtCode.getText()));
                hashProduit.remove(Integer.parseInt(jTxtCode.getText()),r);
                //On vide tout
                jTblRep.clearSelection();
                remplitFiche("");
                //Affichage nouvelle liste
                remplitTable(); 
                typAcc = type_acces.visualisation;
        
            }
        } else { // Sinon, on affiche un message d'alerte
            
        }
                
    }
    
